    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        try {
            // TODO add your handling code here:
            String type = (String) jComboBox1.getSelectedItem();
            char[] userPass = jPasswordField1.getPassword();
            String pass = String.valueOf(userPass);
            pst = (PreparedStatement) con.prepareStatement("select * from member where name=? and password=? and type=?");
            pst.setString(1,jTextField1.getText());
            pst.setString(2,pass);
            pst.setString(3,type);
            rs=pst.executeQuery();
            
            if(rs.next()){
                if(rs.getString("type").equals("admin") ){
                JOptionPane.showMessageDialog(this, "Username and Password matched and you are logged in as an Admin" );
                dispose();
                Main m = new Main();
                m.setVisible(true);
                
                    
                
                
                }else{
                JOptionPane.showMessageDialog(this, "Username and Password matched and you are logged in as Student" );
                dispose();
                StudentHome sh = new StudentHome();
                sh.setVisible(true);
                                
                
                }
            }else{
                
                JOptionPane.showMessageDialog(this, "Error Occured!!!!" );
                jTextField1.setText("");
                jPasswordField1.setText("");
                jComboBox1.setSelectedIndex(-1);
                jTextField1.requestFocus();

                
            }
            

        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

